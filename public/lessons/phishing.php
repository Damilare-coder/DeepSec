    <h1 class="text-center">Understanding Phishing</h1>
    <p>Phishing is a type of cyber attack that attempts to steal sensitive information such as account credentials and credit card numbers by masquerading as a trustworthy entity in electronic communications.</p>
    
    <h2>Characteristics of Phishing</h2>
    <ul>
        <li>Urgent requests for personal information</li>
        <li>Generic greetings (e.g., "Dear Customer")</li>
        <li>Links to fraudulent websites</li>
        <li>Attachments that may contain malware</li>
    </ul>

    <h2>Prevention Methods</h2>
    <p>To protect yourself from phishing attacks, consider the following tips:</p>
    <ul>
        <li>Always verify the sender's email address.</li>
        <li>Do not click on links or download attachments from unknown sources.</li>
        <li>Use two-factor authentication whenever possible.</li>
        <li>Keep your software and antivirus programs updated.</li>
    </ul>

